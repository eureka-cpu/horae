//! Generates the utility stylesheet (`assets/css/horae-utils.css`) from the
//! design scale. The Node-free equivalent of a Tailwind build step.
//!
//! Token *values* live in `horae.css` `:root`; utilities only reference them via
//! `var(--…)`, so this file owns the class matrix, not the palette. Breakpoint
//! pixel values are literals because CSS custom properties cannot appear in
//! `@media` conditions — keep them in sync with the convention in DESIGN.md.

use std::fmt::Write as _;

/// Output path, relative to the crate root (`CARGO_MANIFEST_DIR`).
const OUT: &str = "assets/css/horae-utils.css";

/// Responsive breakpoints: `(prefix, min-width px)`. Must match DESIGN.md.
const BREAKPOINTS: &[(&str, u32)] = &[("sm", 640), ("md", 768), ("lg", 1024)];

fn main() {
    // Only re-run when the generator itself changes, not on every build.
    println!("cargo:rerun-if-changed=build.rs");

    let css = stylesheet();
    let manifest_dir = std::env::var("CARGO_MANIFEST_DIR").unwrap();
    let out_path = std::path::Path::new(&manifest_dir).join(OUT);

    // Compare against the existing file. If identical, skip the write to avoid
    // unnecessary rebuilds and to work in read-only environments (nix sandbox).
    if let Ok(existing) = std::fs::read_to_string(&out_path)
        && existing == css
    {
        return;
    }

    match std::fs::write(&out_path, &css) {
        Ok(()) => println!("cargo:warning=regenerated {OUT} ({} bytes)", css.len()),
        Err(e) => {
            // In a read-only sandbox the write fails. If the file existed but was
            // stale, that's a real problem — the committed file is out of date.
            panic!(
                "cannot write {}: {e}\n\
                 If this is a sandboxed build, regenerate locally:\n  \
                 cargo build -p horae && git add {OUT}",
                out_path.display()
            );
        }
    }
}

// ── CSS generation (ported from crates/cssgen) ──────────────────────────────

struct Family {
    title: &'static str,
    rules: Vec<(String, String)>,
}

fn stylesheet() -> String {
    let mut css = String::new();
    css.push_str(HEADER);
    for family in families() {
        let _ = writeln!(css, "\n/* {} */", family.title);
        for (class, decls) in family.rules {
            let _ = writeln!(css, ".{class} {{ {decls}; }}");
        }
    }
    css.push_str(&responsive());
    css
}

const HEADER: &str = "\
/* ────────────────────────────────────────────────────────────────────────
   GENERATED by build.rs — do not edit by hand.

   A Tailwind-style utility layer, but plain CSS backed by the design tokens in
   horae.css (`--space-*`, `--color-*`, …). The numeric spacing scale matches
   Tailwind's (`p-4` = --space-4 = 1rem). Compose utilities in markup; reach for
   a semantic component class (`.btn`, `.card`, `.badge`) for anything reused.
   ──────────────────────────────────────────────────────────────────────── */
";

fn space() -> Vec<(&'static str, String)> {
    let mut v = vec![("0", "0".to_string())];
    for n in ["1", "2", "3", "4", "6", "8"] {
        v.push((n, format!("var(--space-{n})")));
    }
    v
}

